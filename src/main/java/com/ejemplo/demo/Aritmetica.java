package com.ejemplo.demo;

public class Aritmetica {

    private int numA;
    private int numB;

    public int sumar() {
        return numA + numB;
    }

    public int getNumA() {
        return this.numA;
    }

    public void setNumA(int numA) {
        this.numA = numA;
    }

    public int getNumB() {
        return this.numB;
    }

    public void setNumB(int numB) {
        this.numB = numB;
    }
}